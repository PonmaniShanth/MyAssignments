public class IsPrimeNumber {

	public static void main(String[] args) {
			int num = 9;
			int count=0;
			for (int i = 2; i <num; i++) {
				count=1;
				
			}
			if (count==0) {
				System.out.println("Is a Prime Number" + num);
			}
		else {
					System.out.println("Its not a prime number" + num);
				}
	}
}




	